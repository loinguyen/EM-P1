package vn.com.lco.dto;

import java.util.Date;

/**
 * @author OaiPanda
 *
 * UserChangeLogDTO.java
 */
public class UserChangeLogDTO {

	private String changeUid;
	
	private String affectedUid;
	
	private String reason;
	
	private Date expriedDate;
	
	private Date createdDate;

	/**
	 * @return the changeUid
	 */
	public String getChangeUid() {
		return changeUid;
	}

	/**
	 * @param changeUid the changeUid to set
	 */
	public void setChangeUid(String changeUid) {
		this.changeUid = changeUid;
	}

	/**
	 * @return the affectedUid
	 */
	public String getAffectedUid() {
		return affectedUid;
	}

	/**
	 * @param affectedUid the affectedUid to set
	 */
	public void setAffectedUid(String affectedUid) {
		this.affectedUid = affectedUid;
	}

	/**
	 * @return the reason
	 */
	public String getReason() {
		return reason;
	}

	/**
	 * @param reason the reason to set
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}

	/**
	 * @return the expriedDate
	 */
	public Date getExpriedDate() {
		return expriedDate;
	}

	/**
	 * @param expriedDate the expriedDate to set
	 */
	public void setExpriedDate(Date expriedDate) {
		this.expriedDate = expriedDate;
	}

	/**
	 * @return the createdDate
	 */
	public Date getCreatedDate() {
		return createdDate;
	}

	/**
	 * @param createdDate the createdDate to set
	 */
	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}
	
}
