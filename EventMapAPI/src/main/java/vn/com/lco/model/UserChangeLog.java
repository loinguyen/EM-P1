package vn.com.lco.model;

import java.util.Date;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

/**
 * @author OaiPanda
 *
 * UserChangeLog.java
 */

@Document
public class UserChangeLog {

	@Id
	private String id;
	
	private String changeUId;
	
	private String affectedUId;
	
	private String reason;
	
	private Date expiredDate;
	
	private Date createdDate;

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the changeUId
	 */
	public String getChangeUId() {
		return changeUId;
	}

	/**
	 * @param changeUId the changeUId to set
	 */
	public void setChangeUId(String changeUId) {
		this.changeUId = changeUId;
	}

	/**
	 * @return the affectedUId
	 */
	public String getAffectedUId() {
		return affectedUId;
	}

	/**
	 * @param affectedUId the affectedUId to set
	 */
	public void setAffectedUId(String affectedUId) {
		this.affectedUId = affectedUId;
	}

	/**
	 * @return the reason
	 */
	public String getReason() {
		return reason;
	}

	/**
	 * @param reason the reason to set
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}

	/**
	 * @return the expiredDate
	 */
	public Date getExpiredDate() {
		return expiredDate;
	}

	/**
	 * @param expiredDate the expiredDate to set
	 */
	public void setExpiredDate(Date expiredDate) {
		this.expiredDate = expiredDate;
	}

	/**
	 * @return the createdDate
	 */
	public Date getCreatedDate() {
		return createdDate;
	}

	/**
	 * @param createdDate the createdDate to set
	 */
	public void setCreatedDate(Date createdDate) {
		this.createdDate = createdDate;
	}
	
}
